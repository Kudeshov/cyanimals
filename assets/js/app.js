function detectLanguage() {
  const saved = localStorage.getItem('catsSiteLang');
  if (saved && translations[saved]) return saved;
  const nav = (navigator.language || navigator.userLanguage || 'ru').toLowerCase();
  if (nav.startsWith('en')) return 'en';
  if (nav.startsWith('el')) return 'el';
  return 'ru';
}

function t(key) {
  return translations[currentLang]?.[key] ?? translations.ru[key] ?? key;
}

function localizeCityName(city) {
  return cityTranslations[city]?.[currentLang] || city;
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('catsSiteLang', lang);
  document.documentElement.lang = lang;
  document.title = t('site_title');
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (el.dataset.i18nFormat === 'paragraphs') {
      renderI18nParagraphs(el, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('.lang-btn').forEach((btn) => btn.classList.toggle('active', btn.dataset.lang === lang));
  renderNews();
  populateCityFilter();
  renderIncidents();
  renderCitySummary();
  renderMainMap();
}

function renderI18nParagraphs(el, text) {
  el.innerHTML = '';
  text.split(/\n\n+/).map((paragraph) => paragraph.trim()).filter(Boolean).forEach((paragraph) => {
    const p = document.createElement('p');
    p.textContent = paragraph;
    el.appendChild(p);
  });
}

function pluralizeRu(count, one, few, many) {
  const n = Math.abs(count) % 100;
  const n1 = n % 10;
  if (n > 10 && n < 20) return many;
  if (n1 > 1 && n1 < 5) return few;
  if (n1 === 1) return one;
  return many;
}

function countLabel(count) {
  if (currentLang === 'ru') return `${count} ${pluralizeRu(count, t('city_count_one'), t('city_count_few'), t('city_count_many'))}`;
  return `${count} ${count === 1 ? t('city_count_one') : t('city_count_many')}`;
}

function getLocalizedField(obj, field) {
  if (!obj) return '';
  const value = obj[field];
  if (!value) return '';
  if (typeof value === 'string') return value;
  return value[currentLang] || value.ru || value.en || value.el || '';
}

function typeLabel(type) {
  return t(`type_${type}`) || type;
}

function statusLabel(status) {
  return t(`status_${status}`) || status;
}

function badgeClass(prefix, value) {
  return `badge-${value || prefix}`;
}

function dateLocale() {
  if (currentLang === 'ru') return 'ru-RU';
  if (currentLang === 'el') return 'el-CY';
  return 'en-GB';
}

function parseDate(value) {
  if (!value) return null;
  const normalized = /^\d{4}-\d{2}-\d{2}$/.test(value) ? `${value}T00:00:00` : value;
  const date = new Date(normalized);
  return Number.isNaN(date.getTime()) ? null : date;
}

function dateSortValue(value) {
  return parseDate(value)?.getTime() || 0;
}

function formatNewsDate(value) {
  const date = parseDate(value);
  if (!date) return value || '';
  return new Intl.DateTimeFormat(dateLocale(), {
    day: 'numeric',
    month: 'long',
    year: 'numeric'
  }).format(date);
}

async function loadNews() {
  try {
    const res = await fetch('news.json');
    if (!res.ok) throw new Error(`Could not load news.json: ${res.status}`);
    const data = await res.json();
    newsItems = Array.isArray(data) ? data : [];
  } catch (err) {
    newsItems = [];
    console.warn('Could not load news.json', err);
  }
  renderNews();
}

function renderNews() {
  const list = document.getElementById('newsList');
  const tpl = document.getElementById('newsCardTemplate');
  if (!list || !tpl) return;

  list.innerHTML = '';
  const visibleNews = [...newsItems]
    .sort((a, b) => dateSortValue(b.publishedAt || b.date) - dateSortValue(a.publishedAt || a.date))
    .slice(0, NEWS_VISIBLE_LIMIT);

  if (!visibleNews.length) {
    const empty = document.createElement('p');
    empty.className = 'news-empty';
    empty.textContent = t('news_empty');
    list.appendChild(empty);
    return;
  }

  visibleNews.forEach((item) => {
    const node = tpl.content.cloneNode(true);
    const card = node.querySelector('.news-card');
    const date = node.querySelector('.news-date');
    const title = node.querySelector('.news-title');
    const summary = node.querySelector('.news-summary');
    const details = node.querySelector('.news-details');
    const detailsText = node.querySelector('.news-details-text');
    const detailsSummary = details?.querySelector('summary');
    const gallery = node.querySelector('.news-gallery');
    const links = node.querySelector('.news-links');

    if (item.id) card.dataset.newsId = item.id;
    date.textContent = formatNewsDate(item.date || item.publishedAt);
    title.textContent = getLocalizedField(item, 'title');
    const summaryText = getLocalizedField(item, 'summary');
    const bodyText = getLocalizedField(item, 'body') || getLocalizedField(item, 'description');
    summary.textContent = summaryText;

    if (detailsSummary) detailsSummary.textContent = t('news_open');
    if (bodyText.trim() && bodyText.trim() !== summaryText.trim()) {
      bodyText.split('\n\n').map((p) => p.trim()).filter(Boolean).forEach((paragraph) => {
        const p = document.createElement('p');
        p.textContent = paragraph;
        detailsText.appendChild(p);
      });
    } else {
      details.remove();
    }

    renderNewsGallery(item, gallery);
    renderNewsLinks(item, links);
    list.appendChild(node);
  });
}

function renderNewsGallery(item, gallery) {
  const images = Array.isArray(item.images) ? item.images : [];
  images.forEach((image) => {
    const src = typeof image === 'string' ? image : image?.src;
    if (!src) return;

    const link = document.createElement('a');
    link.href = src;
    link.target = '_blank';
    link.rel = 'noopener';

    const img = document.createElement('img');
    img.src = src;
    img.loading = 'lazy';
    img.alt = typeof image === 'string' ? getLocalizedField(item, 'title') : getLocalizedField(image, 'alt') || getLocalizedField(item, 'title');
    img.addEventListener('error', () => link.remove(), { once: true });

    link.appendChild(img);
    gallery.appendChild(link);
  });
}

function renderNewsLinks(item, linksWrap) {
  const links = Array.isArray(item.links) ? item.links : [];
  const fallbackLinks = item.url ? [{ url: item.url, label: item.linkLabel }] : [];

  [...links, ...fallbackLinks].forEach((linkItem) => {
    const url = typeof linkItem === 'string' ? linkItem : linkItem?.url;
    if (!url) return;

    const link = document.createElement('a');
    link.className = 'news-link';
    link.href = url;
    link.target = '_blank';
    link.rel = 'noopener';
    link.textContent = typeof linkItem === 'string' ? t('news_link_label') : getLocalizedField(linkItem, 'label') || t('news_link_label');
    linksWrap.appendChild(link);
  });
}

async function loadIncidents() {
  const res = await fetch('incidents.json');
  incidents = await res.json();
  populateCityFilter();
  renderIncidents();
  renderCitySummary();
  initMainMap();
  attachForm();
}

function populateCityFilter() {
  const select = document.getElementById('cityFilter');
  if (!select) return;

  const selectedValue = select.value || 'all';
  const cities = [...new Set(incidents.map((item) => item.city))].sort();
  select.innerHTML = `<option value="all">${t('filter_all')}</option>` + cities.map((city) => `<option value="${city}">${localizeCityName(city)}</option>`).join('');
  select.value = cities.includes(selectedValue) || selectedValue === 'all' ? selectedValue : 'all';

  if (!select.dataset.bound) {
    select.addEventListener('change', () => {
      renderIncidents();
      renderMainMap();
    });
    select.dataset.bound = '1';
  }

  const typeFilter = document.getElementById('typeFilter');
  if (typeFilter && !typeFilter.dataset.bound) {
    typeFilter.addEventListener('change', () => {
      renderIncidents();
      renderMainMap();
    });
    typeFilter.dataset.bound = '1';
  }
}

function getFilteredIncidents() {
  const cityValue = document.getElementById('cityFilter')?.value || 'all';
  const typeValue = document.getElementById('typeFilter')?.value || 'all';
  return incidents.filter((item) => (cityValue === 'all' || item.city === cityValue) && (typeValue === 'all' || item.type === typeValue));
}

function renderIncidents() {
  const list = document.getElementById('incidentList');
  if (!list) return;
  list.innerHTML = '';
  detailMaps.length = 0;
  const filtered = getFilteredIncidents();
  const tpl = document.getElementById('incidentCardTemplate');

  filtered.forEach((item, index) => {
    const node = tpl.content.cloneNode(true);
    const card = node.querySelector('.incident-card');
    card.setAttribute('data-incident-id', item.id);
    const typeBadge = node.querySelector('.incident-type');
    const statusBadge = node.querySelector('.incident-status');
    const title = node.querySelector('.incident-title');
    const subtitle = node.querySelector('.incident-subtitle');
    const summary = node.querySelector('.incident-summary');
    const description = node.querySelector('.incident-description');
    const gallery = node.querySelector('.incident-gallery');
    const mapEl = node.querySelector('.incident-map');
    const details = node.querySelector('.incident-details');
    const summaryEl = node.querySelector('summary');
    summaryEl.textContent = t('card_open');

    typeBadge.textContent = typeLabel(item.type);
    typeBadge.classList.add(badgeClass('type', item.type));
    statusBadge.textContent = statusLabel(item.status);
    statusBadge.classList.add(badgeClass('status', item.status));
    title.textContent = getLocalizedField(item, 'title');
    subtitle.textContent = `${t('card_city')}: ${localizeCityName(item.city)} • ${t('card_date')}: ${item.date}`;
    summary.textContent = getLocalizedField(item, 'summary');

    const paragraphs = getLocalizedField(item, 'description').split('\n\n').filter(Boolean);
    description.innerHTML = paragraphs.map((p) => `<p>${p}</p>`).join('') + `<p><em>${t('card_map_note')}</em></p>`;

    if (Array.isArray(item.images) && item.images.length) {
      gallery.innerHTML = item.images.map((img) => {
        const ext = img.split('.').pop().toLowerCase();
        if (['mp4', 'avi', 'mov', 'webm', 'm4v'].includes(ext)) {
          return `<video src="${img}" controls preload="metadata" style="max-width: 100%; height: auto;"></video>`;
        } else {
          return `<a href="${img}" target="_blank" rel="noopener"><img src="${img}" alt="${getLocalizedField(item, 'title')}" loading="lazy"></a>`;
        }
      }).join('');
    } else {
      gallery.innerHTML = `<p>${t('no_photos')}</p>`;
    }

    const mapId = `incident-map-${index}`;
    mapEl.id = mapId;
    details.addEventListener('toggle', () => {
      if (details.open && !details.dataset.mapReady) {
        const map = L.map(mapId, { scrollWheelZoom: false, attributionControl: false }).setView([item.lat, item.lng], 14);
        L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
          attribution: '&copy; OpenStreetMap contributors'
        }).addTo(map);
        L.circle([item.lat, item.lng], {
          radius: item.approximate ? 200 : 60,
          color: colorByType(item.type),
          fillOpacity: 0.2
        }).addTo(map);
        L.marker([item.lat, item.lng]).addTo(map);
        details.dataset.mapReady = '1';
        detailMaps.push(map);
        setTimeout(() => map.invalidateSize(), 50);
      }
    });

    list.appendChild(node);
  });
}

function colorByType(type) {
  if (type === 'shooting') return '#a33a32';
  if (type === 'poisoning') return '#2c7a50';
  return '#9b6a12';
}

function renderCitySummary() {
  const wrap = document.getElementById('citySummary');
  if (!wrap) return;
  const counts = incidents.reduce((acc, item) => {
    acc[item.city] = (acc[item.city] || 0) + 1;
    return acc;
  }, {});
  const cities = Object.keys(counts).sort();
  wrap.innerHTML = cities.map((city) => `
    <article class="city-card">
      <h3>${localizeCityName(city)}</h3>
      <p>${countLabel(counts[city])}</p>
    </article>
  `).join('');
}

function initMainMap() {
  mainMap = L.map('mainMap', { scrollWheelZoom: true, attributionControl: false }).setView([35.1264, 33.4299], 9);
  L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    attribution: '&copy; OpenStreetMap contributors'
  }).addTo(mainMap);
  mainMarkersLayer = L.layerGroup().addTo(mainMap);
  renderMainMap();
}

function renderMainMap() {
  if (!mainMap || !mainMarkersLayer) return;
  mainMarkersLayer.clearLayers();
  const filtered = getFilteredIncidents();
  const bounds = [];
  filtered.forEach((item) => {
    const marker = L.circleMarker([item.lat, item.lng], {
      radius: 8,
      color: colorByType(item.type),
      fillColor: colorByType(item.type),
      fillOpacity: 0.9,
      weight: 1
    });
    marker.bindPopup(`
      <div class="popup-title">${getLocalizedField(item, 'title')}</div>
      <div class="popup-meta">${localizeCityName(item.city)} • ${item.date} • ${typeLabel(item.type)}</div>
      <div>${getLocalizedField(item, 'summary')}</div>
      <div style="margin-top: 12px; border-top: 1px solid #ddd; padding-top: 8px;">
        <a href="#incidents" onclick="const card = document.querySelector('[data-incident-id=&quot;${item.id}&quot;]'); card?.scrollIntoView({ behavior: 'smooth', block: 'start' }); card?.querySelector('details')?.setAttribute('open', 'open'); return false;" style="color: #2f6f49; font-weight: 600; text-decoration: none; cursor: pointer;">
          ${t('popup_link')} →
        </a>
      </div>
    `);
    marker.addTo(mainMarkersLayer);
    bounds.push([item.lat, item.lng]);
  });
  if (bounds.length) {
    mainMap.fitBounds(bounds, { padding: [40, 40] });
  } else {
    mainMap.setView([35.1264, 33.4299], 9);
  }
}

function attachForm() {
  const form = document.getElementById('reportForm');
  const status = document.getElementById('formStatus');
  if (!form) return;

  form.addEventListener('submit', async (e) => {
    e.preventDefault();
    status.textContent = t('form_sending');
    const formData = new FormData(form);
    try {
      const response = await fetch(form.action, {
        method: 'POST',
        body: formData
      });
      const result = await response.json();
      if (result.success) {
        status.textContent = t('form_ok');
        form.reset();
      } else {
        status.textContent = t('form_fail');
      }
    } catch (err) {
      status.textContent = t('form_fail');
    }
  });
}

