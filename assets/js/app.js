function detectLanguage() {
  const saved = localStorage.getItem('catsSiteLang');
  if (saved && translations[saved]) return saved;
  const nav = (navigator.language || navigator.userLanguage || 'ru').toLowerCase();
  if (nav.startsWith('en')) return 'en';
  if (nav.startsWith('el')) return 'el';
  return 'ru';
}

function t(key) {
  return translations[currentLang]?.[key] ?? translations.ru[key] ?? key;
}

function localizeCityName(city) {
  return cityTranslations[city]?.[currentLang] || city;
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('catsSiteLang', lang);
  document.documentElement.lang = lang;
  document.title = t('site_title');
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (el.dataset.i18nFormat === 'paragraphs') {
      renderI18nParagraphs(el, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('.lang-btn').forEach((btn) => btn.classList.toggle('active', btn.dataset.lang === lang));
  populateCityFilter();
  renderNews();
  renderIncidents();
  renderCitySummary();
  renderMainMap();
}

function renderI18nParagraphs(el, text) {
  el.innerHTML = '';
  text.split(/\n\n+/).map((paragraph) => paragraph.trim()).filter(Boolean).forEach((paragraph) => {
    const p = document.createElement('p');
    p.textContent = paragraph;
    el.appendChild(p);
  });
}

function pluralizeRu(count, one, few, many) {
  const n = Math.abs(count) % 100;
  const n1 = n % 10;
  if (n > 10 && n < 20) return many;
  if (n1 > 1 && n1 < 5) return few;
  if (n1 === 1) return one;
  return many;
}

function countLabel(count) {
  if (currentLang === 'ru') return `${count} ${pluralizeRu(count, t('city_count_one'), t('city_count_few'), t('city_count_many'))}`;
  return `${count} ${count === 1 ? t('city_count_one') : t('city_count_many')}`;
}

function getLocalizedField(obj, field) {
  if (!obj) return '';
  const value = obj[field];
  if (!value) return '';
  if (typeof value === 'string') return value;
  return value[currentLang] || value.ru || value.en || value.el || '';
}

